#Un corpus es una colección de textos escritos o hablados que se recopila y almacena para su posterior estudio y análisis. 
# Estos textos pueden abarcar una amplia gama de fuentes, como libros, artículos, conversaciones, transcripciones, páginas web, redes sociales y más.


# Crear un corpus simple de oraciones
corpus = [
    "Este es un ejemplo de un corpus de oraciones.",
    "Un corpus es una colección de texto utilizada en NLP.",
    "Se pueden realizar análisis lingüísticos en un corpus de texto.",
    "Los corpus son fundamentales para entrenar modelos de lenguaje."
]

# Imprimir el corpus
for oracion in corpus:
    print(oracion)